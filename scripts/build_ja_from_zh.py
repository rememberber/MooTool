def zh_to_ja(text: str) -> str:
    result = text
    for zh, ja in ZH_TO_JA:
        result = result.replace(zh, ja)
    return result


def main() -> None:
    zh_lines = ZH.read_text(encoding="utf-8").splitlines()
    en_map = {}
    for line in EN.read_text(encoding="utf-8").splitlines():
        if line and not line.lstrip().startswith("#") and "=" in line:
            k, v = line.split("=", 1)
            en_map[k] = v

    out: list[str] = []
    for line in zh_lines:
        if not line or line.lstrip().startswith("#") or "=" not in line:
            out.append(line)
            continue
        key, value = line.split("=", 1)
        if key in LANGUAGE_OVERRIDES:
            out.append(f"{key}={LANGUAGE_OVERRIDES[key]}")
            continue
        if key.startswith("about."):
            out.append(f"{key}={en_map.get(key, value)}")
            continue
        if key in en_map and value == en_map[key]:
            out.append(f"{key}={value}")
        else:
            out.append(f"{key}={zh_to_ja(value)}")
    JA.write_text("\n".join(out) + "\n", encoding="utf-8")
    print(f"Wrote {JA} ({len(out)} lines)")


if __name__ == "__main__":
    main()
